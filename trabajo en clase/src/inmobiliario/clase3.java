    private void JB_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_aceptarActionPerformed
        
        
        
        int numPisos = Integer.parseInt(jTextField1.getText());
        int numHabitaciones = Integer.parseInt(jTextField2.getText());
        int numBanos = Integer.parseInt(jTextField3.getText());
        
        String tipoInmueble=clase2.getTipoInmueble();
        
        boolean esUrbano = jRadioButton1.isSelected();

        double valorCompra = 1;

        if (tipoInmueble.equals("casa") && esUrbano) {
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, true);
        } else if(tipoInmueble.equals("casa") && !esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, false);
        } else if(tipoInmueble.equals("apartamento") && esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, true);
        } else if(tipoInmueble.equals("apartamento") && !esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, false);
        }else if(tipoInmueble.equals("apartaestudio") && esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, true);
        }else if(tipoInmueble.equals("apartaestudio") && !esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, false);
        }else if(tipoInmueble.equals("conjunto") && esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, true);
        }else if(tipoInmueble.equals("conjunto") && !esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, false);
        }else if(tipoInmueble.equals("condominio") && esUrbano){
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, true);
        }else {
            valorCompra = calcularValorCompra(numHabitaciones, numBanos, numPisos, false);
        }
        
        jTextField4.setText(Double.toString(valorCompra));
    }//GEN-LAST:event_JB_aceptarActionPerformed

    private double calcularValorCompra(int numHabitaciones, int numBanos, int numPisos, boolean esUrbano) {
        double area=clase2.getArea();
        
        double valorCompraT = 0;
       if (esUrbano) {
             if (numPisos == 1) {
                valorCompraT = 3000000 * area; // Casa
            } else {
                valorCompraT = 2500000 * area; // Casa en conjunto 
            }
        } else {
            if (numPisos == 1) {
                valorCompraT = 1500000 * area; // Apartaestudio
            } else {
                valorCompraT = 2000000 * area; // Apartamento 
            }
        }

        return valorCompraT;
    }

