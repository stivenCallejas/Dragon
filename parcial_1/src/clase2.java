    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        
     String textoCorriente = corriente.getText();
    int cantidadCorriente = Integer.parseInt(textoCorriente);

    // Obtener el modelo de datos de tabla2
    DefaultTableModel model = (DefaultTableModel) clase3.tabla2.getModel();

    int seleccionar = tablita.getSelectedRow();
    String[] RegistroDatos = new String[5]; // Supongo que hay 3 columnas en tabla2
    RegistroDatos[0] = tablita.getValueAt(seleccionar, 0).toString();
    RegistroDatos[1] = tablita.getValueAt(seleccionar, 1).toString();
    RegistroDatos[2] = String.valueOf(cantidadCorriente); // Agregar la cantidad
    
    // Calcular el subtotal multiplicando el valor del plato por la cantidad
    int valorPlato = Integer.parseInt(tablita.getValueAt(seleccionar, 1).toString());
    int subtotal = valorPlato * cantidadCorriente;
    RegistroDatos[3] = String.valueOf(subtotal); 
    
    model.addRow(RegistroDatos);
    clase3.tabla2.setModel(model);
    
    // Verificar la opci√≥n seleccionada en la tabla
        String nombrePlato = tablita.getValueAt(seleccionar, 0).toString();
        if (nombrePlato.equals("corriente")) {
            // Muestra la imagen "corriente.jpg"
            ImageIcon icono = new ImageIcon("corriente.jpg");
            imagenLabel.setIcon(icono);
        } else if (nombrePlato.equals("ejecutivo")) {
            // Muestra la imagen "ejecutivo.jpg"
            ImageIcon icono = new ImageIcon("ejecutivo.jpg");
            imagenLabel.setIcon(icono);
        } else {
            ImageIcon icono = new ImageIcon("especial.jpg");
            
            imagenLabel.setIcon(icono);
        }
    
    
    }//GEN-LAST:event_aceptarActionPerformed


